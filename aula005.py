'''
Tipo de dados bool (boolean)
Ao questional algo em um programa, só existem duas respostas possíveis: sim (True) ou não (False). Existem vários operadores para "questionar", dentre eles o ==, que é o operador lógico que questiona se um valor é igual a outro
'''
