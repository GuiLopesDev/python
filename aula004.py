'''
 Tipos int e float 
 int -> Número inteiro
 o tipo int representa qualquer número positivo ou negativo. int sem sinal é considerado positivo.
'''
print(11) #int
print(-11) #int
print(0) #int

'''
 float -> Número com ponto flutuante
 O tipo float representa qualquer número postivio ou negativo com ponto flutuante floar sem sinal é considerado positivo
'''
print(1.1, 10.11)
print(1.2, -2.3, 0.0)

# A função type mostra o tipo que o Python inferiu ao valor

print(type('Teste'))
print(type(123), type(-123), type(0))
print(type(1.23))